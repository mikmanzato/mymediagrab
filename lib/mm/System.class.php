namespace mm;


//------------------------------------------------------------------------------
//! System-related functions
//------------------------------------------------------------------------------
abstract class System
{
	//------------------------------------------------------------------------------
	//! Return the fully-qualified hostname
	//------------------------------------------------------------------------------
	public static function getFqdn()
	{
		return Shell::exec('hostname -f');
	}
};
